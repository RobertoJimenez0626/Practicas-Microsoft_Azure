# String - Cadena de texto
nombre = "Roberto"
print(nombre)

# Int - Enteros, numeros sin punto decimal
edad = 21
edads = "26" # ejemplo de error

print(edad+edad)
print(edads + edads) # resultado del error "2626"

# Float - Flotantes, numeros con punto decimal
pi = 3.141592
edad = float(edad) # Casteo - transformar un tipo de variable a otro

print(pi)
print(edad)

# Usa type() para saber de que tipo es la variable
print(type(nombre), type(edad))

# Bool - Booleano, Si o No
diaLluvioso = False
diaSoleado = True

print(diaLluvioso, diaSoleado)