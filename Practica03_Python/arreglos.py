# Imprimir lista normal
listaNumeros = [1, 2, 3, 4, 5]
print(listaNumeros)

# Imprimiendo elemento con Indice (index)
listaNombres = ["Pepe", "Antonio", "Roberto", "Diego"]
print(listaNombres[3])

# Imprimiendo el largo de una lista
listaElementos = [1, 6, "Luis", 3, "Armando", "Laura"]
print(len(listaElementos))

# Agregando elemento
listaFrutas1 = ["Manzana", "Melon", "Uvas", "Sandia"]
listaFrutas1.append("Mango")
print(listaFrutas1)

# Eliminando elemento
listaFrutas2 = ["Manzana", "Melon", "Uvas", "Sandia"]
listaFrutas2.pop(0)
print(listaFrutas2)
# Recordemos que los elementos de array empiezan a contar desde 0